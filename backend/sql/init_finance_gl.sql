    coutsysname VARCHAR(255) COMMENT '外部凭证系统名称',
    coutsysver VARCHAR(255) COMMENT '外部凭证系统版本号',
    doutbilldate DATETIME COMMENT '外部凭证制单日期',
    ioutperiod TINYINT COMMENT '外部凭证会计期间',
