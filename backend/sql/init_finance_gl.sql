    cdept_id VARCHAR(255) COMMENT '部门编码',
    cperson_id VARCHAR(255) COMMENT '职员编码',
    ccus_id VARCHAR(255) COMMENT '客户编码',
    csup_id VARCHAR(255) COMMENT '供应商编码',
