    bcodeedit TINYINT(1) COMMENT '分录科目是否可修改',
    ccodecontrol VARCHAR(255) COMMENT '分录受控科目可用状态',
    bPCSedit TINYINT(1) COMMENT '分录往来项是否可修改',
    bDeptedit TINYINT(1) COMMENT '分录部门是否可修改',
    bItemedit TINYINT(1) COMMENT '分录项目是否可修改',
    bCusSupInput TINYINT(1) COMMENT '分录往来项是否必输',
