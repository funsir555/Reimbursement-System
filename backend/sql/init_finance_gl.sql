    cbegind_c VARCHAR(255) COMMENT '金额期初方向',
    cbegind_c_engl VARCHAR(255) COMMENT '金额期初方向英文',
    ccode VARCHAR(255) COMMENT '科目编码',
    cendd_c VARCHAR(255) COMMENT '金额期末方向',
    cendd_c_engl VARCHAR(255) COMMENT '金额期末方向英文',
    cexch_name VARCHAR(255) COMMENT '币种名称',
    i_id INT NOT NULL AUTO_INCREMENT COMMENT '自动编号',
    iperiod TINYINT COMMENT '会计期间',
    company_id VARCHAR(64) COMMENT '公司主体编码',
    mb DECIMAL(18,2) COMMENT '金额期初',
    mb_f DECIMAL(18,2) COMMENT '外币期初',
    mc DECIMAL(18,2) COMMENT '贷方金额',
    mc_f DECIMAL(18,2) COMMENT '外币贷方金额',
    md DECIMAL(18,2) COMMENT '借方金额',
    md_f DECIMAL(18,2) COMMENT '外币借方金额',
    me DECIMAL(18,2) COMMENT '金额期末',
    me_f DECIMAL(18,2) COMMENT '外币期末',
    nb_s DECIMAL(18,6) COMMENT '数量期初',
    nc_s DECIMAL(18,6) COMMENT '数量贷方',
    nd_s DECIMAL(18,6) COMMENT '数量借方',
