    cDefine1 VARCHAR(255) COMMENT '自定义项1',
    cDefine2 VARCHAR(255) COMMENT '自定义项2',
    cDefine3 VARCHAR(255) COMMENT '自定义项3',
    cDefine4 DATETIME COMMENT '自定义项4',
    cDefine5 INT COMMENT '自定义项5',
