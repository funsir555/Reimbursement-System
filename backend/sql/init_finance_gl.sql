    ino_id SMALLINT COMMENT '凭证编号',
    inid SMALLINT COMMENT '行号',
    dbill_date DATETIME COMMENT '制单日期',
    idoc SMALLINT COMMENT '附单据数',
    cbill VARCHAR(255) COMMENT '制单人',
    ccheck VARCHAR(255) COMMENT '审核人',
    cbook VARCHAR(255) COMMENT '记账人',
    ibook TINYINT COMMENT '记账标志',
    ccashier VARCHAR(255) COMMENT '出纳签字人',
    iflag TINYINT COMMENT '凭证标志',
    ctext1 VARCHAR(255) COMMENT '凭证头自定义项1',
    ctext2 VARCHAR(255) COMMENT '凭证头自定义项2',
    cdigest VARCHAR(255) COMMENT '摘要',
    ccode VARCHAR(255) COMMENT '科目编码',
    cexch_name VARCHAR(255) COMMENT '币种名称',
    md DECIMAL(18,2) COMMENT '借方金额',
    mc DECIMAL(18,2) COMMENT '贷方金额',
