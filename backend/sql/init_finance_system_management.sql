USE finex_db;

SET NAMES utf8mb4;

CREATE TABLE IF NOT EXISTS fin_account_set (
    company_id VARCHAR(64) NOT NULL COMMENT '公司主体编码',
    status VARCHAR(16) NOT NULL DEFAULT 'INITIALIZING' COMMENT '账套状态',
    enabled_year INT NULL COMMENT '启用年度',
    enabled_period INT NULL COMMENT '启用期间',
    template_code VARCHAR(64) NULL COMMENT '模板编码',
    supervisor_user_id BIGINT NULL COMMENT '账套主管用户ID',
    create_mode VARCHAR(16) NOT NULL DEFAULT 'BLANK' COMMENT '创建方式',
    reference_company_id VARCHAR(64) NULL COMMENT '参照账套公司',
    subject_code_scheme VARCHAR(64) NULL COMMENT '科目编码规则',
    subject_count INT NOT NULL DEFAULT 0 COMMENT '科目数量',
    last_task_no VARCHAR(64) NULL COMMENT '最近任务号',
    error_message VARCHAR(500) NULL COMMENT '失败原因',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (company_id),
    KEY idx_fin_account_set_status (status),
    KEY idx_fin_account_set_template (template_code),
    KEY idx_fin_account_set_supervisor (supervisor_user_id),
    KEY idx_fin_account_set_reference (reference_company_id),
    CONSTRAINT fk_fin_account_set_company FOREIGN KEY (company_id) REFERENCES sys_company(company_id),
    CONSTRAINT fk_fin_account_set_reference_company FOREIGN KEY (reference_company_id) REFERENCES sys_company(company_id),
    CONSTRAINT fk_fin_account_set_supervisor FOREIGN KEY (supervisor_user_id) REFERENCES sys_user(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='财务账套主表';

CREATE TABLE IF NOT EXISTS fin_account_set_code_rule (
    id BIGINT NOT NULL AUTO_INCREMENT COMMENT '主键',
    company_id VARCHAR(64) NOT NULL COMMENT '公司主体编码',
    rule_type VARCHAR(64) NOT NULL COMMENT '规则类型',
    scheme VARCHAR(64) NOT NULL COMMENT '规则表达式',
    level1_length INT NOT NULL DEFAULT 4 COMMENT '一级科目长度',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (id),
    UNIQUE KEY uk_fin_account_set_code_rule (company_id, rule_type),
    KEY idx_fin_account_set_code_rule_company (company_id),
    CONSTRAINT fk_fin_account_set_code_rule_company FOREIGN KEY (company_id) REFERENCES sys_company(company_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='账套编码规则';

CREATE TABLE IF NOT EXISTS fin_account_set_template (
    template_code VARCHAR(64) NOT NULL COMMENT '模板编码',
    template_name VARCHAR(128) NOT NULL COMMENT '模板名称',
    accounting_standard VARCHAR(128) NOT NULL COMMENT '制度口径',
    description VARCHAR(500) NULL COMMENT '模板说明',
    status TINYINT NOT NULL DEFAULT 1 COMMENT '状态',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (template_code),
    KEY idx_fin_account_set_template_status (status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='账套模板主表';

CREATE TABLE IF NOT EXISTS fin_account_set_template_subject (
    id BIGINT NOT NULL AUTO_INCREMENT COMMENT '主键',
    template_code VARCHAR(64) NOT NULL COMMENT '模板编码',
    subject_key VARCHAR(128) NOT NULL COMMENT '模板科目键',
    parent_subject_key VARCHAR(128) NULL COMMENT '父级模板科目键',
    subject_level INT NOT NULL COMMENT '科目层级',
    level_segment VARCHAR(32) NOT NULL COMMENT '当前级编码段',
    subject_name VARCHAR(128) NOT NULL COMMENT '科目名称',
    balance_direction VARCHAR(16) NOT NULL COMMENT '余额方向',
    subject_category VARCHAR(32) NOT NULL COMMENT '科目类别',
    leaf_flag TINYINT NOT NULL DEFAULT 1 COMMENT '是否末级',
    sort_order INT NOT NULL DEFAULT 0 COMMENT '排序号',
    status TINYINT NOT NULL DEFAULT 1 COMMENT '状态',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (id),
    UNIQUE KEY uk_fin_account_set_template_subject (template_code, subject_key),
    KEY idx_fin_account_set_template_subject_parent (template_code, parent_subject_key),
    KEY idx_fin_account_set_template_subject_sort (template_code, sort_order),
    CONSTRAINT fk_fin_account_set_template_subject_template FOREIGN KEY (template_code) REFERENCES fin_account_set_template(template_code)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='账套模板科目';

CREATE TABLE IF NOT EXISTS fin_account_subject (
    id BIGINT NOT NULL AUTO_INCREMENT COMMENT '主键',
    company_id VARCHAR(64) NOT NULL COMMENT '公司主体编码',
    subject_code VARCHAR(64) NOT NULL COMMENT '科目编码',
    subject_name VARCHAR(128) NOT NULL COMMENT '科目名称',
    parent_subject_code VARCHAR(64) NULL COMMENT '父级科目编码',
    subject_level INT NOT NULL COMMENT '科目层级',
    balance_direction VARCHAR(16) NOT NULL COMMENT '余额方向',
    subject_category VARCHAR(32) NOT NULL COMMENT '科目类别',
    leaf_flag TINYINT NOT NULL DEFAULT 1 COMMENT '是否末级',
    status TINYINT NOT NULL DEFAULT 1 COMMENT '状态',
    template_code VARCHAR(64) NULL COMMENT '来源模板编码',
    sort_order INT NOT NULL DEFAULT 0 COMMENT '排序号',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (id),
    UNIQUE KEY uk_fin_account_subject (company_id, subject_code),
    KEY idx_fin_account_subject_company_level (company_id, subject_level, sort_order),
    KEY idx_fin_account_subject_company_parent (company_id, parent_subject_code),
    CONSTRAINT fk_fin_account_subject_company FOREIGN KEY (company_id) REFERENCES sys_company(company_id),
    CONSTRAINT fk_fin_account_subject_template FOREIGN KEY (template_code) REFERENCES fin_account_set_template(template_code)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='公司级会计科目';

